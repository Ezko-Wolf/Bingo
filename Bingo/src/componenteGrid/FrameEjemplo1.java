    private void generar() {
        int filas = Integer.parseInt(tfFilas.getText());
        int columnas = Integer.parseInt(tfColumnas.getText());
        int paneles = Integer.parseInt(tfPaneles.getText());
        generar(filas,columnas,paneles);
    }
    private void generar(int filas, int columnas, int cantidadPaneles){
        
        Object[] datos = new Object[filas*columnas];
        for(int x=0;x<datos.length;x++){
            datos[x] = (x+1) + "";
        }
        ListaPaneles listaPaneles = new ListaPaneles(filas,columnas);
        
        int i=0;
        while(i++<cantidadPaneles){
            try {
               
                listaPaneles.agregarPanel(datos, this);
            } catch (GridLayoutException ex) {
                System.out.println(ex.getMessage());
                System.exit(0);
            }
        }
        areaScroll.setViewportView(listaPaneles);
        listaPaneles.marcar();
    }
